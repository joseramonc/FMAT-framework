package fmat.arquitectura.DBAccess.Exceptions;

public class DBConfigXMLException extends Exception {

	public DBConfigXMLException() {
		super();
	}

	public DBConfigXMLException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DBConfigXMLException(String message, Throwable cause) {
		super(message, cause);
	}

	public DBConfigXMLException(String message) {
		super(message);
	}

	public DBConfigXMLException(Throwable cause) {
		super(cause);
	}
	
}
